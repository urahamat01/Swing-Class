    private void dispTArea() {

        FileInputStream fodj = null;
        fc = new JFileChooser(dir);
        int option = fc.showOpenDialog(this);
        if (option == JFileChooser.APPROVE_OPTION) {

            file = fc.getSelectedFile();
            dir = fc.getCurrentDirectory();

            try {
                String str = "";
                fodj = new FileInputStream(file);
                int len = (int) file.length();
                for (int i = 0; i < len; i++) {
                    char str2 = 0;
                    try {
                        str2 = (char) fodj.read();
                        if (str2 == ',') {
                            str2 = '\t';

                        }
                    } catch (IOException e) {
                    }

                    str = str + str2;

                }
                txtArea.setText(str);
            } catch (Exception e) {
                e.printStackTrace();
            } finally {
                try {
                    if (fodj != null) {
                        fodj.close();

                    }
                } catch (Exception e) {
                }
            }

        }

    }

