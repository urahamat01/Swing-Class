    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        //savae file
        getAllRecord();
        if (txtName.getText().isEmpty()) {

            JOptionPane.showMessageDialog(null, "Entered your name :");
            txtName.requestFocus();
        } else if (txtAddress.getText().isEmpty()) {

            JOptionPane.showMessageDialog(null, "Address is empty");
            txtAddress.requestFocus();

        } else if (txtCountry.getItemAt(txtCountry.getSelectedIndex()) == "select A round") {

            JOptionPane.showMessageDialog(null, "Entered your country :");
        } else if (gender.getSelection().isSelected() == false) {

            JOptionPane.showMessageDialog(null, "select a gender");

        } else if (!txtJava.isSelected() && !txtPhp.isSelected() && !txtDatabase.isSelected()) {

            JOptionPane.showMessageDialog(null, "select your corse :");
        } else {

            if (txtCountry.getSelectedIndex() == 0) {

                JOptionPane.showMessageDialog(null, "select your country ;");
                txtCountry.requestFocus();
            } else if (txtArea.getText().length() < 5) {
                JOptionPane.showMessageDialog(null, "entered your note");

            } else {
                fc = new JFileChooser(dir);

                FileNameExtensionFilter fielter;
                fielter = new FileNameExtensionFilter("*.txt", new String[]{"txt"});
                fc.addChoosableFileFilter(fielter);
                int option = fc.showSaveDialog(this);
                if (option == JFileChooser.APPROVE_OPTION) {
                    dir = fc.getCurrentDirectory();
                    model.addRow(new Object[]{fna, addr, coun, gend, degree});
                    file = fc.getSelectedFile();

                    try {
                        writeAll();
                    } catch (IOException ex) {
                        ex.printStackTrace();
                    }
                    degree = "";

                }

            }

        }

    }//GEN-LAST:event_btnSaveActionPerformed

