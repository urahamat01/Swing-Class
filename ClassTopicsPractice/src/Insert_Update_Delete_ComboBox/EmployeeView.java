    public void displayDataIntoTable() {
        clearTable();
        EmployeeDao employeeDao = new EmployeeDaoImp();
        DefaultTableModel model = (DefaultTableModel) jTableDisplay.getModel();
        List<Employee> list = employeeDao.getEmployees();
        Object cols[] = new Object[3];
        for (int i = 0; i < list.size(); i++) {
            cols[0] = list.get(i).getId();
            cols[1] = list.get(i).getName();
            cols[2] = list.get(i).getRole();
            model.addRow(cols);
        }
    }

    public void clearTable() {
        DefaultTableModel model = (DefaultTableModel) jTableDisplay.getModel();
        model.setRowCount(0);
    }

