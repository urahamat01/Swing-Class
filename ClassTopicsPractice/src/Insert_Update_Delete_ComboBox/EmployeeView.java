    private void jButtonInsert1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonInsert1ActionPerformed
//fields
        int id = Integer.parseInt(jTextFieldId.getText().trim());
        String name = jTextFieldName.getText().trim();
        String role = jTextFieldRole.getText().trim();
        //validation
        if (jTextFieldId.getText().trim().length() < 3) {
            JOptionPane.showMessageDialog(null, "Id should be atleast 3 characters!");
        } else if (name.length() < 4) {
            JOptionPane.showMessageDialog(null, "Name should be atleast 4 characters!");

        } else if (role.length() < 5) {
            JOptionPane.showMessageDialog(null, "Role  should be atleast 5 characters!");

        } else {

            EmployeeDao employeeDao = new EmployeeDaoImp();
            ///
            try {
                Employee exitEmployee = employeeDao.getEmployeeById(id);
                if (exitEmployee.getName() != null) {
                    JOptionPane.showMessageDialog(null, "Id Already exists!");

                } else {
                    Employee emp = new Employee(id, name, role);
                    employeeDao.insert(emp);
                    displayDataIntoTable();
                    JOptionPane.showMessageDialog(null, "Data successfully inserted into employee table!");
                }
            } catch (Exception e) {
                Employee emp = new Employee(id, name, role);
                employeeDao.insert(emp);
                displayDataIntoTable();
                JOptionPane.showMessageDialog(null, "Data successfully inserted into employee table!");

            }
            displayDataIntoComboBox();

        }

        //inser to the database
        //display into the table

    }//GEN-LAST:event_jButtonInsert1ActionPerformed

    private void jButtonUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonUpdateActionPerformed
        EmployeeDao empDao = new EmployeeDaoImp();
        try {
            Employee empCheck = empDao.getEmployeeById(selectedId);
            if (empCheck.getName() != null) {
                Employee emp = new Employee(selectedId, jTextFieldName.getText().trim(), jTextFieldRole.getText().trim());
                empDao.update(emp);
                displayDataIntoTable();
                JOptionPane.showMessageDialog(null, "Data successfully udpated for id: " + selectedId + " into employee table!");

            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Please Enter data to update!");

        }


    }//GEN-LAST:event_jButtonUpdateActionPerformed
